    }

    override fun augmentStat(imbueLevel: Int): AugmentDatapoint {
        return AugmentDatapoint(SpellType.FURY,18,4,
            9,imbueLevel,1,LoreTier.LOW_TIER, Items.IRON_SWORD)
    }

